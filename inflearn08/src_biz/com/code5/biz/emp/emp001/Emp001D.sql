--[[[ EMP001D_01



SELECT
AA.EMP_N
, AA.EMP_NM
, AA.HP_N DEC__HP_N
, AA.DEPT_N
, AA.FILE_ID "TOKEN__FILE_ID , downloadfile"
, BB.FILE_NAME 
FROM EMP AA 
LEFT OUTER JOIN FW_UPLOAD_FILE BB ON AA.FILE_ID = BB.FILE_ID 
WHERE EMP_NM LIKE [EMP_NM]||'%' 
ORDER BY EMP_N;

--]]]


