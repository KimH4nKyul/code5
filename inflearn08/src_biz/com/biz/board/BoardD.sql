--[[[ LIST, TIMEOUT=10, LIMIT=10

SELECT 
N "TOENC__N, list"
, TITLE
, RG_DTM DTM__DTM
, RG_ID
FROM
BZ_BOARD
WHERE 1 = 1
[~ TITLE ^ IS_NOT_NULL ^ AND TITLE LIKE '%'||[TITLE]||'%' ~]
[~ NEXT_N ^ IS_NOT_NULL ^ AND N > [NEXT_N] ~]
AND DEL_Y IS NULL
ORDER BY N


--]]]

